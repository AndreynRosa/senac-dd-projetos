    public ConsultaServicoProva() {
        initComponents();
        BuscarTB();
        //Carregar os dados no ComboBox, onde cbGrupo é o nome do componente JComboBox
        GrupoProdutoMOCDAO daoGrupo = new GrupoProdutoMOCDAO();
        List<GrupoProduto> lista = daoGrupo.listarPorTipo(TipoProduto.SERVICO,"");
        DefaultComboBoxModel comboModel = new DefaultComboBoxModel(lista.toArray());
        combBoxGrup.setModel(comboModel);
        combBoxGrup.setSelectedItem(null); //Nenhum selecionado

        //Evento disparado quando um registro do grid é selecionado, onde tbServico é o nome do JTable
        tbServico.getSelectionModel().addListSelectionListener(new ListSelectionListener() {
        public void valueChanged(ListSelectionEvent event) {
            //Indica que o usuário ainda não concluiu a seleção do registro
                if (event.getValueIsAdjusting()) {
                    return;
                }
                int indexRegSel = event.getFirstIndex();
                if (tbServico.getSelectedRow() != -1) {

            //Faça o DataBinding dos quatro campos da tela
            //1 - Obtenha o id do registro selecionado no grid
             
            Long idServico = (Long) tbServico.getValueAt(indexRegSel, 0);
            ServicoProva servProvDB  = serDAO.getPorId(idServico);
            
            //2 - Busque o objeto serviço do DAO
            
            
            fieldNome.setText(servProvDB.getNomeProduto());
            fieldDate.setValue(servProvDB.getDataCriacao());
            fieldISS.setValue(servProvDB.getPercISS());
            //3 - Databinding: transfira os dados do objeto do tipo Servico para tela
           
            //Dicas
            //Para o JFormattedTextField (Data Criação e Perc ISS), utilize o método setValue
            combBoxGrup.setSelectedItem(servProvDB.getGrupoProduto());
            //Para o JComboBox (Grupo), utilize o método setSelectedItem
                }
            }
        });
    }

