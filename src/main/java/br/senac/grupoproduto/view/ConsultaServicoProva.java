    public void BuscarTB(){
        DefaultTableModel model =(DefaultTableModel) tbServico.getModel();
        model.setRowCount(0);
        for(ProdutoProva prodProv: serDAO.listarTodos()){
            Object[] row = new Object[4];
            row[0] = prodProv.getIdProduto();
            row[1] = prodProv.getNomeProduto();
            row[2] = prodProv.getGrupoProduto().getNomeGrupoProduto();
            row[3] = prodProv.getPercICMS();
            model.addRow(row);
            
        }
    }
    
    public void Config(boolean edicao){
        if(edicao == true){//edicao
            btnNovo.setVisible(!edicao);
            btnGravar.setVisible(edicao);
            btnExcluir.setVisible(!edicao);
            btnAlterar.setVisible(!edicao);
        }else{// Consulta
             fieldNome.setEditable(edicao);
             BuscarTB();
        }
   
       
        
    }
