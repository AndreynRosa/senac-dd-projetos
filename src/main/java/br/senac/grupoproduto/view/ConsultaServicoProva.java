    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        Integer  indexRow = tbServico.getSelectedRow();
        if(indexRow == -1)
            JOptionPane.showMessageDialog(this, "Selecione uma registro da tabela","Anteção",
                    JOptionPane.WARNING_MESSAGE);
        else{
          long idSerivco = (Integer)tbServico.getModel().getValueAt(indexRow,0);
          int pergunta = JOptionPane.showConfirmDialog(this, "Confirma Exclusão","Exclusão!!!",
                  JOptionPane.YES_OPTION);
                  if(pergunta == JOptionPane.YES_NO_OPTION){
                      serDAO.excluir(idSerivco);
                  }
        }
            
    }//GEN-LAST:event_btnExcluirActionPerformed

